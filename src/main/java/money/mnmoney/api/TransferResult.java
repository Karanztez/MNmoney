package money.mnmoney.api;

/**
 * ผลลัพธ์ของการทำธุรกรรมการโอนเงิน
 */
public enum TransferResult {
    /**
     * การโอนเงินสำเร็จ
     */
    SUCCESS,
    /**
     * ผู้ส่งมีเงินไม่เพียงพอ
     */
    INSUFFICIENT_FUNDS,
    /**
     * จำนวนเงินไม่ถูกต้อง (น้อยกว่าหรือเท่ากับ 0)
     */
    INVALID_AMOUNT,
    /**
     * ผู้ส่งและผู้รับเป็นคนเดียวกัน
     */
    SELF_TRANSFER,
    /**
     * เกิดข้อผิดพลาดที่ไม่คาดคิดระหว่างการทำธุรกรรม
     */
    ERROR
}
